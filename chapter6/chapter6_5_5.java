package chapter6;

public class chapter6_5_5 {
	public static void main(String[] args) {
        String s="F";
//        switch可解析字符串
        switch(s){
        case"A":
        	System.out.println('A');
        break;
        case"B":
        	System.out.println('B');
        	break;
        case"F":
        	System.out.println(s);
        }
	}
}
